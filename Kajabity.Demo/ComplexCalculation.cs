namespace Kajabity.Demo
{
    /// <summary>
    /// The ComplexCalculation class provides useful mathematics methods.
    /// </summary>
    public class ComplexCalculation
    {
        /// <summary>
        /// Multiplies a value by two.
        /// </summary>
        /// <param name="value">the value to multiply</param>
        /// <returns>the value multiplied by two.</returns>
        public int TimesTwo(int value)
        {
            return value * 2;
        }
    }
}
